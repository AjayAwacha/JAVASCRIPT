const prefixIncrement = ++a;
let b = 20;
const prefixDecrement = --b;
let c = 10;
const postfixIncrement = c++;
let d = 20;
const postfixDecrement = d--;
console.log(prefixIncrement, a);
console.log(prefixDecrement, b);
console.log(postfixIncrement, c);
console.log(postfixDecrement, d);

